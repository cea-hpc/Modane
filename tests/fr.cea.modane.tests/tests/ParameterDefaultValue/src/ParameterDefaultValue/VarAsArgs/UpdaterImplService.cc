UpdaterImplService::
UpdaterImplService(const ServiceBuildInfo& bi)
: UpdaterImplServiceBase<UpdaterImplService>(bi)
{
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

UpdaterImplService::
~UpdaterImplService()
{
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

Real UpdaterImplService::
f1(UpdaterImplF1Vars& vars, const Real a)
{
	return a;
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

Real UpdaterImplService::
f2(UpdaterImplF2Vars& vars, const Real a, const Integer b)
{
	return a * b;
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

Real UpdaterImplService::
f3(UpdaterImplF3Vars& vars, const Real a, const Integer b, const bool c)
{
	if (c)
		return a * b;
	else
		return 0;
}

